"""Core test case models for analytics evaluation.

Design principles:
- Immutable data models via Pydantic (frozen=True)
- All fields optional except `input` — eval framework degrades gracefully
- Extensible via `metadata` dict for custom fields
- Supports both single-turn and multi-turn (conversational) evaluation
"""

from __future__ import annotations

from enum import StrEnum
from typing import Any

import pandas as pd
from pydantic import BaseModel, Field, field_validator


class Difficulty(StrEnum):
    """Question difficulty levels, matching BIRD and Spider conventions."""

    SIMPLE = "simple"
    MODERATE = "moderate"
    CHALLENGING = "challenging"


class AnalyticsTestCase(BaseModel):
    """Captures the full context of a single analytics interaction.

    This is the primary data model for evaluation. Every metric reads from
    this case to compute its score. Fields are optional because not every
    evaluation dimension requires every field — the framework degrades
    gracefully, skipping metrics that lack required data.

    Design decisions:
    - Pydantic BaseModel for validation, serialization, and IDE support
    - frozen=False because pipeline stages populate fields incrementally
    - expected_sql + actual_sql: enables SQL-level comparison
    - expected_results + actual_results: enables data-level comparison
    - ir_manifest: enables our unique Join Path Correctness metric
    - metadata: extensibility point for custom fields without subclassing
    """

    # ── Required fields ──
    input: str = Field(
        ...,
        description="Natural language question from the user",
        min_length=1,
    )

    # ── Ground truth fields ──
    db_id: str | None = Field(
        default=None,
        description="Database identifier for the target warehouse",
    )
    expected_sql: str | None = Field(
        default=None,
        description="Ground truth SQL (from benchmark or compiler)",
    )
    expected_results: pd.DataFrame | None = Field(
        default=None,
        description="Ground truth result set",
    )
    evidence: str | None = Field(
        default=None,
        description="External knowledge hints (BIRD-style)",
    )
    difficulty: Difficulty | None = Field(
        default=None,
        description="Difficulty level: simple, moderate, challenging",
    )

    # ── Agent output fields ──
    actual_sql: str | None = Field(
        default=None,
        description="SQL generated by the agent under test",
    )
    actual_results: pd.DataFrame | None = Field(
        default=None,
        description="Agent result set after execution",
    )
    insight_text: str | None = Field(
        default=None,
        description="Natural language insight from the agent",
    )
    chart_spec: dict[str, Any] | None = Field(
        default=None,
        description="Chart specification (type, axes, data mapping)",
    )

    # ── Semantic context fields ──
    semantic_context: dict[str, Any] | None = Field(
        default=None,
        description="Semantic model context provided to agent",
    )
    ir_manifest: dict[str, Any] | None = Field(
        default=None,
        description="SIR/LIR/AIR compilation manifest from the compiler",
    )

    # ── Execution metadata ──
    latency_ms: float | None = Field(
        default=None,
        description="Query execution latency in milliseconds",
        ge=0,
    )
    token_count: int | None = Field(
        default=None,
        description="Total tokens consumed by the agent",
        ge=0,
    )
    cost_usd: float | None = Field(
        default=None,
        description="Estimated cost in USD",
        ge=0,
    )

    # ── Extensibility ──
    metadata: dict[str, Any] = Field(
        default_factory=dict,
        description="Additional metadata (source, tags, custom fields)",
    )

    model_config = {"arbitrary_types_allowed": True}

    @field_validator("input")
    @classmethod
    def input_must_not_be_blank(cls, v: str) -> str:
        if not v.strip():
            raise ValueError("input must not be blank")
        return v

    def has_expected_sql(self) -> bool:
        """Check if ground truth SQL is available."""
        return self.expected_sql is not None and len(self.expected_sql.strip()) > 0

    def has_actual_sql(self) -> bool:
        """Check if agent SQL is available."""
        return self.actual_sql is not None and len(self.actual_sql.strip()) > 0

    def has_expected_results(self) -> bool:
        """Check if ground truth results are available."""
        return self.expected_results is not None and not self.expected_results.empty

    def has_actual_results(self) -> bool:
        """Check if agent results are available."""
        return self.actual_results is not None and not self.actual_results.empty

    def has_ir_manifest(self) -> bool:
        """Check if IR manifest is available for join path evaluation."""
        return self.ir_manifest is not None and len(self.ir_manifest) > 0

    def has_insight(self) -> bool:
        """Check if agent produced a natural language insight."""
        return self.insight_text is not None and len(self.insight_text.strip()) > 0

    def has_chart(self) -> bool:
        """Check if agent produced a chart specification."""
        return self.chart_spec is not None and len(self.chart_spec) > 0


class ConversationalAnalyticsCase(BaseModel):
    """Multi-turn conversational analytics test case.

    Captures a sequence of AnalyticsTestCases representing a multi-turn
    conversation between a user and an analytics agent. This enables
    evaluation of conversation-level properties like metric consistency
    across turns, knowledge retention, and follow-up question handling.
    """

    turns: list[AnalyticsTestCase] = Field(
        ...,
        description="Ordered sequence of analytics interactions",
        min_length=1,
    )
    conversation_id: str | None = Field(
        default=None,
        description="Unique identifier for the conversation",
    )
    metadata: dict[str, Any] = Field(
        default_factory=dict,
        description="Additional conversation-level metadata",
    )

    model_config = {"arbitrary_types_allowed": True}

    @property
    def num_turns(self) -> int:
        return len(self.turns)
