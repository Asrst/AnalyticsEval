"""Agent interface definitions for BYOA evaluation.

Design principles:
- Minimal interface: one async method is all that's needed
- AgentResponse captures everything the framework needs to evaluate
- Built-in adapters for common patterns (DirectSQL, Registry, MCP)
- Framework never assumes how the agent works internally
"""

from __future__ import annotations

from abc import ABC, abstractmethod
from typing import Any

from pydantic import BaseModel, Field


class AgentResponse(BaseModel):
    """The output of an analytics agent for a single query.

    This is what the agent returns when asked a question. The framework
    uses these fields to populate AnalyticsTestCase fields before
    running metrics.

    Design: all fields optional except actual_sql — an agent that doesn't
    produce SQL is not an analytics agent. Everything else (insights, charts)
    is optional because agents vary in capability.
    """

    actual_sql: str | None = Field(
        default=None,
        description="SQL generated by the agent",
    )
    actual_results: Any | None = Field(
        default=None,
        description="Query results (will be converted to DataFrame)",
    )
    insight_text: str | None = Field(
        default=None,
        description="Natural language insight/explanation",
    )
    chart_spec: dict[str, Any] | None = Field(
        default=None,
        description="Chart specification if the agent generates "
        "visualizations",
    )
    latency_ms: float | None = Field(
        default=None,
        description="Time taken by the agent in milliseconds",
        ge=0,
    )
    token_count: int | None = Field(
        default=None,
        description="Total tokens consumed",
        ge=0,
    )
    cost_usd: float | None = Field(
        default=None,
        description="Estimated cost in USD",
        ge=0,
    )
    error: str | None = Field(
        default=None,
        description="Error message if the agent failed",
    )
    metadata: dict[str, Any] = Field(
        default_factory=dict,
        description="Agent-specific metadata",
    )


class AgentInterface(ABC):
    """Abstract interface that any analytics agent must implement.

    The BYOA (Bring Your Own Agent) model means any agent can be evaluated
    by implementing this single interface. No need to switch tools, adopt
    our framework, or change your agent's architecture.

    Usage:
        class MyAgent(AgentInterface):
            async def query(self, question, db_id, context=None):
                sql = generate_sql(question, db_id)
                results = execute_sql(sql, db_id)
                return AgentResponse(actual_sql=sql, actual_results=results)

        agent = MyAgent()
        pipeline = EvalPipeline(agent=agent, metrics=[ExecutionAccuracy()])
        results = pipeline.evaluate(dataset)
    """

    @abstractmethod
    async def query(
        self,
        question: str,
        db_id: str,
        context: dict[str, Any] | None = None,
    ) -> AgentResponse:
        """Process a natural language question and return an agent response.

        Args:
            question: The natural language question from the user.
            db_id: Database identifier for the target warehouse.
            context: Optional semantic context (metric definitions, schema).

        Returns:
            AgentResponse with the agent's SQL, results, insights, metadata.
        """
        ...

    @property
    def name(self) -> str:
        """Human-readable agent name for reporting."""
        return self.__class__.__name__
